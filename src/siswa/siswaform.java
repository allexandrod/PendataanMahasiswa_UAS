    private void createTableIfNotExists() {
        try (Connection conn = Koneksi.getConnection()) {
            String sql = "CREATE TABLE IF NOT EXISTS siswa ("
                    + "id INTEGER PRIMARY KEY AUTOINCREMENT,"
                    + "nama TEXT, nim TEXT, alamat TEXT, jenis_kelamin TEXT)";
            Statement stmt = conn.createStatement();
            stmt.execute(sql);
        } catch (SQLException e) {
            System.out.println("Gagal membuat tabel: " + e.getMessage());
        }
    }

    private void tampilData() {
        model = (DefaultTableModel) Tabel_Siswa.getModel();
        model.setRowCount(0); // Kosongkan tabel
        try (Connection conn = Koneksi.getConnection()) {
            String sql = "SELECT * FROM siswa";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            int no = 1;
            while (rs.next()) {
                model.addRow(new Object[]{
                    no++,
                    rs.getString("nama"),
                    rs.getString("nim"),
                    rs.getString("alamat"),
                    rs.getString("jenis_kelamin")
                });
            }
        } catch (SQLException e) {
            System.out.println("Gagal menampilkan data: " + e.getMessage());
        }
    }

