    private void Tabel_SiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabel_SiswaMouseClicked
        int row = Tabel_Siswa.getSelectedRow();
        if (evt.getClickCount() == 1 && row != -1) {
            String nama = Tabel_Siswa.getValueAt(row, 1).toString();
            String nim = Tabel_Siswa.getValueAt(row, 2).toString();
            String alamat = Tabel_Siswa.getValueAt(row, 3).toString();
            String jk = Tabel_Siswa.getValueAt(row, 4).toString();

            int pilihan = javax.swing.JOptionPane.showOptionDialog(
                    this,
                    "Pilih aksi untuk data:\nNIM: " + nim + "\nNama: " + nama,
                    "Aksi Data",
                    javax.swing.JOptionPane.DEFAULT_OPTION,
                    javax.swing.JOptionPane.INFORMATION_MESSAGE,
                    null,
                    new String[]{"Edit", "Hapus", "Batal"},
                    "Edit"
            );

            if (pilihan == 0) { // Edit
                txtNama_Siswa.setText(nama);
                Txt_Nim.setText(nim);
                Txt_Alamat.setText(alamat);
                Slc_Jenis_kelamin.setSelectedItem(jk);

                isEditMode = true;
                selectedNim = nim;
                btnSimpan.setText("Update");

            } else if (pilihan == 1) { // Hapus
                int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(
                        this, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);

                if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
                    try (Connection conn = Koneksi.getConnection()) {
                        String sql = "DELETE FROM siswa WHERE nim=?";
                        PreparedStatement pst = conn.prepareStatement(sql);
                        pst.setString(1, nim);
                        pst.executeUpdate();
                        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                        tampilData();
                    } catch (SQLException e) {
                        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus: " + e.getMessage());
                    }
                }
            }
            // Batal â†’ Tidak lakukan apa pun
        }
    }//GEN-LAST:event_Tabel_SiswaMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtNama_Siswa.getText();
        String nim = Txt_Nim.getText();
        String alamat = Txt_Alamat.getText();
        String jk = Slc_Jenis_kelamin.getSelectedItem().toString();

        if (nama.isEmpty() || nim.isEmpty() || alamat.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Isi semua field!");
            return;
        }

        try (Connection conn = Koneksi.getConnection()) {
            if (isEditMode) {
                // UPDATE
                String sql = "UPDATE siswa SET nama=?, nim=?, alamat=?, jenis_kelamin=? WHERE nim=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, nama);
                pst.setString(2, nim);
                pst.setString(3, alamat);
                pst.setString(4, jk);
                pst.setString(5, selectedNim); // WHERE nim lama
                pst.executeUpdate();
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
            } else {
                // INSERT
                String sql = "INSERT INTO siswa(nama, nim, alamat, jenis_kelamin) VALUES (?, ?, ?, ?)";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, nama);
                pst.setString(2, nim);
                pst.setString(3, alamat);
                pst.setString(4, jk);
                pst.executeUpdate();
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            }

            tampilData();
            clearInput();

        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed
    private void clearInput() {
        txtNama_Siswa.setText("");
        Txt_Nim.setText("");
        Txt_Alamat.setText("");
        Slc_Jenis_kelamin.setSelectedIndex(0);
        isEditMode = false;
        selectedNim = null;
        btnSimpan.setText("Simpan");
    }

